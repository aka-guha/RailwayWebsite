                <h3 class="ir">INDIAN RAILWAYS</h3>
                     <p>Indian Railways is India's national railway system operated by the Ministry of Railways. It manages the fourth largest railway network in the world by size, with a route length of 67,368-kilometre and total track length of 121,407-kilometre as of March 2017.</p>
                    <button type="button" class="btn btn-primary"> Read More>></button>
                    <div class="about-content">
                        The first railway proposals for India were made in Madras in 1832. The country's first train, Red Hill Railway (built by Arthur Cotton to transport granite for road-building), ran from Red Hills to the Chintadripet bridge in Madras in 1837. In 1845, the Godavari Dam Construction Railway was built by Cotton at Dowleswaram in Rajahmundry, to supply stone for the construction of a dam over the Godavari River. In 1851, the Solani Aqueduct Railway was built by Proby Cautley in Roorkee to transport construction materials for an aqueduct over the Solani River.
                        <br>
                        India's first passenger train, hauled by three steam locomotives (Sahib, Sindh and Sultan), ran for 34 kilometres (21 miles) with 400 people in 14 carriages on 1,676 mm (5 ft 6 in) broad gauge track between Bori Bunder (Mumbai) and Thane on 16 April 1853. The Thane viaducts, India's first railway bridges, were built over the Thane creek when the Mumbai-Thane line was extended to Kalyan in May 1854. Eastern India's first passenger train ran 24 miles (39 km) from Howrah, near Kolkata, to Hoogly on 15 August 1854. The first passenger train in South India ran 60 miles (97 km) from Royapuram- Veyasarapady (Madras) to Wallajah Road (Arcot) on 1 July 1856.
                        <br>
                        On 24 February 1873, a horse-drawn 3.8-kilometre (2.4 mi) tram opened in Calcutta between Sealdah and Armenian Ghat Street. On 9 May 1874, a horse-drawn tramway began operation in Bombay between Colaba and Parel. In 1897, lighting in passenger coaches was introduced by many railway companies. On 3 February 1925, the first electric passenger train in India ran between Victoria Terminus and Kurla.
                        <br>    
                        The organisation of Indian railways into regional zones began in 1951, when the Southern (14 April 1951), Central (5 November 1951) and Western (5 November 1951) zones were created. Fans and lights were mandated for all compartments in all passenger classes in 1951, and sleeping accommodations were introduced in coaches. In 1956, the first fully air-conditioned train was introduced between Howrah and Delhi. Ten years later, the first containerized freight service began between Mumbai and Ahmedabad. In 1986, computerized ticketing and reservations were introduced in New Delhi. 
